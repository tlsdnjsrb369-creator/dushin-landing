-- 작업(제품) 목록 — 수주통보서 연동 (QR/NFC 태그용)
create table if not exists jobs (
  id        bigint generated always as identity primary key,
  su_no     text unique not null,                          -- 수주번호
  name      text not null,                                 -- 공사명(제품)
  company   text,                                          -- 발주업체
  order_amt bigint default 0,                              -- 발주금액(매출)
  qr_token  text not null default gen_random_uuid()::text unique,
  active    boolean default true
);
alter table jobs enable row level security;

-- 탭 기반 작업세션 (시작/종료 자동기록)
create table if not exists work_sessions (
  id         bigint generated always as identity primary key,
  worker_id  bigint references workers(id) on delete set null,
  job_id     bigint references jobs(id) on delete cascade,
  category   text,                                         -- 공정(제관/용접/사상/조립/보조)
  started_at timestamptz default now(),
  ended_at   timestamptz,
  minutes    int
);
alter table work_sessions enable row level security;

